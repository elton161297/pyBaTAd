  #data type conversion
a="12345"
print(a)
print(type(a))

print("string to integer conversion")
b=int(a)
print(b)
print(type(b))

print("str to complex conversion")
c=complex(a)
print(c)
print(type(c))

print("str to float conversion")
d=float(a)
print(d)
print(type(d))

print("str to list conversion")
e=list(a)
print(e)
print(type(e))

print("str to tuple conversion")
f=tuple(a)
print(f)
print(type(f))

