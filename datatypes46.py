#numeric type
    #int 
print(5)
print(type(5))

    #float
print(25.5)
print(type(25.5))

    #complex
print(2j)
print(type(2j))

#Sequence Types
    #list (or) array
x1 = ["apple", "banana", "cherry"] 
print(x1)
print(type(x1))

   #tuple
x2 = ("apple", "banana", "cherry")
print(x2)
print(type(x2))

    #range
x3 = range(10)
print(x3)
print(type(x3))

#Mapping Type
    #dict
x4 = {"Name":"Elton","Age":"24","Place":"UK"}
print(x4)
print(type(x4))

#Set Types
    #set
x5 = {"apple1", "apple2", "apple3"}
print(x5)
print(type(x5))

    #frozenset
x6 =frozenset ({"cherry1", "cherry2", "cherry3"})
print(x6)
